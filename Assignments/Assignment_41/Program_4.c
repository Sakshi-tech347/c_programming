int factorial(int n)
{
    if (n == 0 || n == 1)   // base condition
        return 1;

    return n * factorial(n - 1);  // recursive call
}
  

///////////////////////////////////////////////////////////////////
//    
//    Entry point Function for the application 
//
///////////////////////////////////////////////////////////////////
 
int main()
{
    int num =0 , iRet =0;

    printf("Enter a number: ");
    scanf("%d", &num);

    iRet =factorial(num);
    printf("Factorial = %d", iRet);
    return 0;
}

