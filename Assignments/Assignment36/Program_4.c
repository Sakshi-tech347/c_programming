void DisplayDigit(char Str[])
{
  int iCount = 0;
  while(*Str != 0)
  {
    if ((*Str >= 0) && (*Str <= 9))
    {
      iCount++;
    }
    Str++;
  }
  printf("number of Digit is %d",iCount);
}
  

///////////////////////////////////////////////////////////////////
//    
//    Entry point Function for the application 
//
///////////////////////////////////////////////////////////////////
 int main()
 {
  char Arr[50]={'\0'};

  printf("enter your string :\n");
  scanf("%[^'\n']s",&Arr);

  DisplayDigit(Arr);

  return 0;
 }

