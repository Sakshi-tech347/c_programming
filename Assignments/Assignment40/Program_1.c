#include <stdio.h>

void Display(int iNo)
{
    if (iNo == 0)        
        return;

    printf("* ");
    Display(iNo - 1);   
}
  

///////////////////////////////////////////////////////////////////
//    
//    Entry point Function for the application 
//
///////////////////////////////////////////////////////////////////
 

int main()
{
    int iValue;

    printf("Enter number of stars: ");
    scanf("%d", &iValue);

   Display(iValue);
    return 0;
}

